#arithmatic operators

print(2+7)
print(22/7)
print(22%%7)
print(2^7)
print(22%/%7) #floor division operator, always gives the previous number, no matter what

#relational operator

var1=50
var2=100
print(var1>var2)


#logical operator

value1=c(TRUE,FALSE,TRUE,FALSE)
value2=c(FALSE,TRUE,TRUE,FALSE)
print(value1&&value2)   #and or operator
print(!value1)    #not operator
